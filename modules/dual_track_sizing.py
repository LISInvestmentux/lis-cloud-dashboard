"""
位階股數 vs 風控股數 雙軌制（Phase 33.2 — 大俠教學）

兩條獨立計算路徑，最後取兩者較小值，避免單軸極端化。

設計理念（大俠 ARK 派的核心）：
  - 位階股數：依「股價在歷史位階」決定要多少股
       低位階（深谷） → 多買；高位階（過熱） → 少買
       純粹從「便宜貴」角度
  - 風控股數：依「單檔不能超過總資產 X%」決定上限
       不管多看好，單檔最多 20% 總資產
       保護「all-in 一檔結果踩雷」的最壞情況

  最終建議：min(位階股數, 風控股數)
  → 兩個軸同時把關，不會「太貴還重押」也不會「便宜但超出配置」

範例（NVDA $200，總資產 50 萬）：
  - 位階分數 30（價值區）→ 位階建議 NT$ 30,000 = 4 股
  - 風控上限 20% = NT$ 100,000 = 15 股
  - 最終：4 股（位階決定）

範例（00981A NT$ 30，已持 22% 總資產）：
  - 位階分數 25（深價值）→ 位階建議 NT$ 50,000 = 1,666 股
  - 風控上限 20% - 已持 22% = -2%（已超）→ 0 股
  - 最終：0 股（風控決定 — 已經超持）
"""
from typing import Optional


# 位階分數 → 建議買入比例（% of 可動用彈藥）
位階配重表 = {
    25: 0.20,   # 深價值區（≤25）→ 投 20% 彈藥
    35: 0.12,   # 強進入價值區（25-35）→ 12%
    45: 0.08,   # 進入價值區（35-45）→ 8%
    55: 0.04,   # 中性偏低（45-55）→ 4%
    65: 0.02,   # 中性偏高（55-65）→ 2%
    80: 0.00,   # 偏高（65-80）→ 跳過
    100: 0.00,  # 過熱（80+）→ 跳過
}

# 風控上限（% of 總資產）
單檔總部位上限_pct = 20.0
單檔單日加碼上限_pct = 5.0


def 算位階股數(位階分數: float, 可動用彈藥_twd: float,
                現價_twd: float, 順勢分: float = 0,
                是ETF: bool = False) -> dict:
    """
    依位階決定要買多少股。

    位階低 → 大買；位階高 → 少買 / 跳過。
    ETF + 順勢可破例（即使位階高也可加碼）。
    """
    if 現價_twd <= 0:
        return {"建議股數": 0, "建議金額_twd": 0, "理由": "無價格"}

    # 找對應的配重
    配重 = 0
    for 分界 in sorted(位階配重表.keys()):
        if 位階分數 <= 分界:
            配重 = 位階配重表[分界]
            break

    理由 = []
    if 位階分數 <= 25:
        理由.append(f"深價值區 ({位階分數:.0f}) — 大買")
    elif 位階分數 <= 45:
        理由.append(f"進入價值區 ({位階分數:.0f}) — 分批")
    elif 位階分數 <= 65:
        理由.append(f"中性區 ({位階分數:.0f}) — 紀律")
    else:
        理由.append(f"偏高位階 ({位階分數:.0f}) — 預設跳過")

    # ETF + 順勢救援
    if 配重 == 0 and 是ETF and 順勢分 >= 70:
        配重 = 0.03
        理由.append(f"ETF 順勢強 ({順勢分:.0f}) — 破例 3%")

    建議金額 = 可動用彈藥_twd * 配重
    建議股數 = int(建議金額 // 現價_twd) if 現價_twd > 0 else 0

    return {
        "位階分數": 位階分數,
        "配重_pct": 配重 * 100,
        "建議金額_twd": round(建議金額, 0),
        "建議股數": 建議股數,
        "現價_twd": 現價_twd,
        "理由": " · ".join(理由),
    }


def 算風控股數(已持市值_twd: float, 總資產_twd: float,
                現價_twd: float) -> dict:
    """
    依單檔風控上限決定還能買多少股。

    上限：
      - 單檔總部位 ≤ 20% 總資產
      - 單檔單日加碼 ≤ 5% 總資產
    """
    if 現價_twd <= 0 or 總資產_twd <= 0:
        return {"建議股數": 0, "理由": "無價格或無資產"}

    已持_pct = (已持市值_twd / 總資產_twd * 100) if 總資產_twd > 0 else 0
    可加_pct = max(0, 單檔總部位上限_pct - 已持_pct)
    可加_pct = min(可加_pct, 單檔單日加碼上限_pct)
    可加金額 = 總資產_twd * 可加_pct / 100

    建議股數 = int(可加金額 // 現價_twd) if 現價_twd > 0 else 0

    if 可加_pct == 0:
        理由 = f"已超持 ({已持_pct:.1f}% ≥ 20%) — 不能再加"
    elif 可加_pct < 單檔單日加碼上限_pct:
        理由 = (f"已持 {已持_pct:.1f}%，距上限 20% 還有 "
                f"{單檔總部位上限_pct - 已持_pct:.1f}%")
    else:
        理由 = f"單檔單日加碼上限 {單檔單日加碼上限_pct}%"

    return {
        "已持_pct": round(已持_pct, 2),
        "可加_pct": round(可加_pct, 2),
        "可加金額_twd": round(可加金額, 0),
        "建議股數": 建議股數,
        "現價_twd": 現價_twd,
        "理由": 理由,
    }


def 雙軌計算(symbol: str, 位階分數: float, 順勢分: float = 0,
              是ETF: bool = False,
              現價_twd: float = 0,
              已持市值_twd: float = 0,
              總資產_twd: float = 0,
              可動用彈藥_twd: float = 0) -> dict:
    """
    雙軌制主入口：算位階 + 風控，取小者。

    Returns:
        {
            最終股數: int,        # min(位階, 風控)
            最終金額_twd: float,
            決定軸: "位階" / "風控" / "兩者一致",
            位階: {...},
            風控: {...},
            一句話: str,
        }
    """
    位階 = 算位階股數(位階分數, 可動用彈藥_twd, 現價_twd,
                      順勢分, 是ETF)
    風控 = 算風控股數(已持市值_twd, 總資產_twd, 現價_twd)

    位階_股 = 位階["建議股數"]
    風控_股 = 風控["建議股數"]
    最終 = min(位階_股, 風控_股)

    if 位階_股 == 風控_股:
        決定軸 = "兩者一致"
    elif 位階_股 < 風控_股:
        決定軸 = "位階"
    else:
        決定軸 = "風控"

    最終金額 = 最終 * 現價_twd

    if 最終 == 0:
        if 位階_股 == 0 and 風控_股 == 0:
            一句話 = "❌ 位階偏高 + 已超持 — 兩軸都喊停"
        elif 位階_股 == 0:
            一句話 = f"❌ {位階['理由']} — 位階軸喊停"
        else:
            一句話 = f"❌ {風控['理由']} — 風控軸喊停"
    else:
        一句話 = (f"✅ 買 {最終} 股 ({決定軸}軸決定) "
                   f"= NT$ {最終金額:,.0f}")

    return {
        "symbol": symbol,
        "最終股數": 最終,
        "最終金額_twd": round(最終金額, 0),
        "決定軸": 決定軸,
        "位階": 位階,
        "風控": 風控,
        "一句話": 一句話,
    }


