"""
資金量適配檢查（Phase 33.2）

依 user 的「總資金 + 投入比例」判斷適合什麼戰法。

LIS 派核心觀點：
  - 1 股戰法（高價美股 1-3 股）→ 摩擦成本太高，需要 200 萬以上
  - 整買零賣（國泰優惠）→ 適合任何資金，每筆 NT$ 5,000+ 才划算
  - 台股 ETF（0050, 00646 等）→ 適合 50 萬以下入門
  - ARK 集中重押 → 不適合 200 萬以下（單筆爆損 > 5% 總資金太傷）

警告等級：
  - 紅：資金太小，戰法明顯不適合
  - 黃：邊緣狀況，可做但效率差
  - 綠：完全適合

回傳給 user：
  「你目前 40 萬資金不適合 NVDA 1 股戰法 (摩擦 13%)，建議改 NVDL ETF 或台股 ETF」
"""
from typing import Optional


# 不同戰法的資金門檻（NT$）
戰法門檻 = {
    "1股戰法":     {"建議": 2_000_000, "底限": 800_000,
                    "說明": "高價美股 NVDA/MSTR 1-3 股，摩擦成本最高"},
    "ARK重押":     {"建議": 2_000_000, "底限": 1_000_000,
                    "說明": "單檔重押 5-10%，單筆爆損 >5% 太傷"},
    "美股小單":    {"建議": 500_000,   "底限": 200_000,
                    "說明": "$50-300 美股小單，搭配國泰整買零賣"},
    "整買零賣":    {"建議": 100_000,   "底限": 50_000,
                    "說明": "國泰專屬 0.08% 無低消，任何資金都適合"},
    "台股ETF":     {"建議": 100_000,   "底限": 30_000,
                    "說明": "0050 / 00646 / 00878 分散低費"},
    "DCA定期定額": {"建議": 50_000,    "底限": 10_000,
                    "說明": "每月 5K-10K 自動扣，幾乎沒門檻"},
}


def 算摩擦成本(每筆金額_twd: float, 手續費率: float = 0.001425,
              低消_twd: float = 0) -> dict:
    """
    算單筆摩擦成本。
    手續費率：台股 0.1425% / 美股折扣 0.5%
    """
    手續費 = max(每筆金額_twd * 手續費率, 低消_twd)
    摩擦_pct = (手續費 / 每筆金額_twd * 100) if 每筆金額_twd > 0 else 100
    return {
        "每筆金額": 每筆金額_twd,
        "手續費": round(手續費, 0),
        "摩擦_pct": round(摩擦_pct, 3),
    }


def 戰法適配檢查(總資金_twd: float, 戰法: str) -> dict:
    """
    Args:
        總資金_twd: user 總資金
        戰法: ∈ 戰法門檻 keys

    Returns:
        {
            適配等級: "綠/黃/紅",
            訊息: str,
            建議: str,
        }
    """
    if 戰法 not in 戰法門檻:
        return {"適配等級": "未知", "訊息": f"未知戰法 {戰法}", "建議": ""}

    th = 戰法門檻[戰法]
    建議線 = th["建議"]
    底限 = th["底限"]

    if 總資金_twd >= 建議線:
        return {
            "戰法": 戰法,
            "總資金_twd": 總資金_twd,
            "適配等級": "綠",
            "色": "bull",
            "訊息": f"資金 NT$ {總資金_twd:,.0f} ≥ 建議 {建議線:,.0f} — 完全適合",
            "建議": f"可放心執行「{戰法}」 — {th['說明']}",
        }
    elif 總資金_twd >= 底限:
        return {
            "戰法": 戰法,
            "總資金_twd": 總資金_twd,
            "適配等級": "黃",
            "色": "wait",
            "訊息": f"資金 NT$ {總資金_twd:,.0f} 介於底限-建議線之間",
            "建議": (f"勉強可做「{戰法}」，但效率差。"
                     f"建議達 {建議線:,.0f} 再放開做 — {th['說明']}"),
        }
    else:
        return {
            "戰法": 戰法,
            "總資金_twd": 總資金_twd,
            "適配等級": "紅",
            "色": "bear",
            "訊息": (f"資金 NT$ {總資金_twd:,.0f} < 底限 {底限:,.0f} — "
                     f"明顯不適合"),
            "建議": (f"目前資金不適合「{戰法}」 — {th['說明']}\n"
                     f"建議改：整買零賣（NT$ 100,000 起）或 台股 ETF（NT$ 30,000 起）"),
        }


def 全面適配檢查(總資金_twd: float) -> dict:
    """
    跑完所有戰法，回傳「哪些可做、哪些別碰」。

    Returns:
        {
            可做: [...],
            邊緣: [...],
            別碰: [...],
            推薦: "...",
        }
    """
    可做 = []
    邊緣 = []
    別碰 = []
    for 戰法 in 戰法門檻:
        r = 戰法適配檢查(總資金_twd, 戰法)
        if r["適配等級"] == "綠":
            可做.append(r)
        elif r["適配等級"] == "黃":
            邊緣.append(r)
        else:
            別碰.append(r)

    # 推薦邏輯
    if 總資金_twd >= 2_000_000:
        推薦 = "全戰法可用 — 重點看訊號強弱"
    elif 總資金_twd >= 500_000:
        推薦 = "美股小單 + 台股 ETF 並行 — 避免 1 股戰法（摩擦高）"
    elif 總資金_twd >= 100_000:
        推薦 = "整買零賣 + 台股 ETF 為主 — 不碰 ARK 重押 / 美股 1 股"
    elif 總資金_twd >= 30_000:
        推薦 = "台股 ETF + DCA 定期定額 — 入門期，重複利為先"
    else:
        推薦 = "先累積到 30K 再開始 — 現在以「能存錢」為主"

    return {
        "總資金_twd": 總資金_twd,
        "可做": 可做,
        "邊緣": 邊緣,
        "別碰": 別碰,
        "推薦": 推薦,
    }


def 個案檢查(每筆下單金額_twd: float, 市場: str = "TW",
              券商: str = "國泰整買零賣") -> dict:
    """
    檢查單筆下單的摩擦成本是否合理。
    券商選項：
      - 國泰整買零賣（0.08%，無低消，2026/12/31 前優惠）
      - 國泰一般（0.1425%，低消 NT$ 20）
      - 美股折扣（0.5%，無低消）
      - 美股全額（手續費高，不推薦）
    """
    if 券商 == "國泰整買零賣":
        率 = 0.0008
        低消 = 0
    elif 券商 == "國泰一般":
        率 = 0.001425
        低消 = 20
    elif 券商 == "美股折扣":
        率 = 0.005
        低消 = 0
    else:
        率 = 0.005
        低消 = 0

    摩 = 算摩擦成本(每筆下單金額_twd, 率, 低消)

    if 摩["摩擦_pct"] < 0.2:
        等級 = "綠"; 色 = "bull"
        建議 = "摩擦低，可放心執行"
    elif 摩["摩擦_pct"] < 0.5:
        等級 = "黃"; 色 = "wait"
        建議 = "摩擦中等，可接受但別太頻繁進出"
    else:
        等級 = "紅"; 色 = "bear"
        建議 = "摩擦過高 — 改加大每筆金額 或換低費券商"

    return {
        "每筆下單": 每筆下單金額_twd,
        "市場": 市場,
        "券商": 券商,
        "手續費": 摩["手續費"],
        "摩擦_pct": 摩["摩擦_pct"],
        "等級": 等級,
        "色": 色,
        "建議": 建議,
    }


