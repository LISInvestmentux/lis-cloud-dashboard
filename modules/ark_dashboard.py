"""
ARK 風控對標儀表（Phase 14b）
完全仿太太 ARK App「風控運算」頁面。

核心演算法（從太太截圖反推）：
  建議水位 % = 100 - 風控過熱程度
  實際水位 % = 持股市值 / (持股市值 + 現金) × 100
  差距 = 建議水位 - 實際水位

  差距 > 0：低於建議 → 還能加碼，建議布局金額 = 差距 × 總資金
  差距 ≤ 0：過熱 → 觸發「調節」訊號（強制賣到建議水位）

跟 LIS 的「+15% 個股紀律」**並存**：
  個股紀律 = 單檔達標賣
  ARK 水位 = 整體部位過熱賣（更全局）
"""
from typing import Optional


def 計算建議水位(風控總分: Optional[float]) -> float:
    """
    風控 5 儀表總分 0-100（越高越過熱）→ 建議水位 %（越低）

    映射：
      風控 0   → 建議水位 90%（市場很冷，全進）
      風控 50  → 建議水位 60%（中性）
      風控 100 → 建議水位 20%（極度過熱，幾乎全出）
    """
    if 風控總分 is None:
        return 60.0   # default
    # 線性映射：0→90, 100→20
    建議 = 90 - (風控總分 / 100) * 70
    return round(max(20, min(90, 建議)), 1)


def 計算ARK風控(風控總分: Optional[float],
                  持股市值_twd: float,
                  現金_twd: float,
                  USD_TWD: float = 31.5) -> dict:
    """
    主入口：算 ARK 級風控對標。

    回傳：
    {
      "建議水位_pct": float,
      "實際水位_pct": float,
      "總資金_twd": float,
      "持股市值_twd": float,
      "現金_twd": float,
      "建議持股_twd": float,
      "建議閒錢_twd": float,
      "建議布局金額_twd": float,    # > 0：可加碼，< 0：要調節
      "狀態": "可加碼" / "觀望" / "觸發調節" / "資料不足",
      "動作": str,
    }
    """
    建議水位 = 計算建議水位(風控總分)
    總資金 = 持股市值_twd + 現金_twd
    if 總資金 <= 0:
        return {
            "建議水位_pct": 建議水位,
            "實際水位_pct": 0,
            "狀態": "資料不足",
            "動作": "請先設定持股 + 現金",
        }

    實際水位 = round(持股市值_twd / 總資金 * 100, 2)
    差距 = 建議水位 - 實際水位

    建議持股 = round(總資金 * 建議水位 / 100, 0)
    建議閒錢 = round(總資金 * (100 - 建議水位) / 100, 0)
    建議布局金額 = round(建議持股 - 持股市值_twd, 0)

    if 差距 >= 5:
        狀態 = "🟢 可加碼"
        動作 = f"還能買 NT$ {建議布局金額:,.0f}（差 {差距:.1f}%）"
    elif 差距 >= -2:
        狀態 = "⚖️ 觀望"
        動作 = "已接近建議水位，正常持有"
    else:
        狀態 = "🚨 觸發調節"
        動作 = f"超出建議水位 {abs(差距):.1f}%，建議賣 NT$ {abs(建議布局金額):,.0f}"

    return {
        "建議水位_pct": 建議水位,
        "實際水位_pct": 實際水位,
        "差距_pct": round(差距, 2),
        "總資金_twd": round(總資金, 0),
        "持股市值_twd": round(持股市值_twd, 0),
        "現金_twd": round(現金_twd, 0),
        "建議持股_twd": 建議持股,
        "建議閒錢_twd": 建議閒錢,
        "建議布局金額_twd": 建議布局金額,
        "狀態": 狀態,
        "動作": 動作,
    }


def 從main流程計算(風控: Optional[dict],
                    真倉: Optional[dict],
                    設定: dict,
                    USD_TWD: float = 31.5) -> dict:
    """
    給 main.py 用：自動撈出風控總分、持股、現金。
    """
    風控總分 = 風控.get("總體分數") if 風控 else None

    持股市值 = 0
    if 真倉:
        持股市值 = 真倉.get("總計", {}).get("市值_twd", 0)

    現金_twd = 設定.get("current_cash_twd", 0)
    # 若 portfolio.json 有手填美元現金欄位，也加進去
    # （目前沒這欄位，未來可加）

    return 計算ARK風控(風控總分, 持股市值, 現金_twd, USD_TWD)
