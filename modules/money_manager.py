"""
年度彈藥控管（Phase 17.5）— 修補 LIS 部位建議過度激進的問題

問題：position_sizer 每檔最多 5% × N 檔 = 可能一天部署 70% 現金
理想：依「閒錢比 vs 目標」算「可動用彈藥」，分散到全年

設計：
  總資金 × (1 - 目標閒錢比) = 規劃持股上限
  現金 - 閒錢底線 = 可動用彈藥
  可動用彈藥 ÷ 預期年化部署次數 = 每筆建議規模

模式（依「閒錢比 vs 目標」）：
  > +10% 充足：彈藥充足，積極部署（建議倍率 ×1.5）
  +0 ~ +10% 平衡：紀律分批（建議倍率 ×1.0）
  -5 ~ 0  接近底線：減小單筆（×0.5）
  < -5%   越線：暫停買進（×0）— 只接收 OOO IN 訊號（黑天鵝）

回傳：
  {
    水位: {持股_pct, 現金_pct, 閒錢比_pct, 目標閒錢比_pct},
    模式, 模式說明,
    可動用彈藥_twd,
    建議單日總彈藥_twd,    # 平時一天最多花這麼多
    建議單筆上限_twd,      # 一檔最多花這麼多
    黑天鵝預備金_twd,      # 鎖起來等黑天鵝才用
    年化部署計畫: {月度建議, 季度建議, ...},
  }
"""
from typing import Optional


# 預設常數
預期年化部署次數 = 30    # 一年大概 30 次有意義訊號（每月 2-3 次大決策）
黑天鵝預備金比例 = 0.4   # 可動用彈藥 40% 保留給黑天鵝
單筆下限_twd = 1000      # 太小的單沒意義（手續費比例失衡）
每日訊號數量上限 = 6     # 一天最多吃 6 個訊號的部位量


def 計算可用彈藥(總資金: float,
                  現金: float,
                  持股成本_twd: float,
                  目標閒錢比_pct: float = 35.0,
                  預期年化次數: int = 預期年化部署次數) -> dict:
    """
    依水位算可動用彈藥。
    """
    持股_pct = 持股成本_twd / 總資金 * 100 if 總資金 > 0 else 0
    現金_pct = 現金 / 總資金 * 100 if 總資金 > 0 else 0
    閒錢比_pct = 現金_pct
    差距 = 閒錢比_pct - 目標閒錢比_pct

    # 閒錢底線（絕不該動的部分）
    閒錢底線_twd = 總資金 * 目標閒錢比_pct / 100

    # 可動用彈藥（現金中超過底線的部分）
    可動用彈藥 = max(0, 現金 - 閒錢底線_twd)

    # 黑天鵝預備金
    黑天鵝預備 = 可動用彈藥 * 黑天鵝預備金比例
    平時可用 = 可動用彈藥 - 黑天鵝預備

    # 模式判定
    if 差距 > 10:
        模式 = "充足"
        模式emoji = "🟢"
        模式倍率 = 1.5
        說明 = f"閒錢比 {閒錢比_pct:.1f}% > 目標 {目標閒錢比_pct}% +{差距:.1f}%，積極部署"
    elif 差距 > 0:
        模式 = "平衡"
        模式emoji = "⚪"
        模式倍率 = 1.0
        說明 = f"閒錢比 {閒錢比_pct:.1f}% 剛好（差 +{差距:.1f}%），紀律分批"
    elif 差距 > -5:
        模式 = "接近底線"
        模式emoji = "🟡"
        模式倍率 = 0.5
        說明 = f"閒錢比 {閒錢比_pct:.1f}% < 目標 {目標閒錢比_pct}% ({差距:.1f}%)，減小單筆"
    else:
        模式 = "越線"
        模式emoji = "🔴"
        模式倍率 = 0.0
        說明 = f"閒錢比 {閒錢比_pct:.1f}% 嚴重低於底線 ({差距:.1f}%)，暫停買進"

    # 平時每日彈藥（部位 sizer 套用上限）
    # 邏輯：平時可用 ÷ 預期年化次數 = 每訊號平均規模
    # 一天最多吃 N 個訊號量
    每訊號平均 = 平時可用 / 預期年化次數 if 預期年化次數 > 0 else 0
    建議單日總彈藥 = 每訊號平均 * 每日訊號數量上限 * 模式倍率
    # 但若太少，至少給 NT$ 2000 動一筆
    建議單日總彈藥 = max(建議單日總彈藥, 2000 * 模式倍率)

    # 單筆上限（不超過單日總彈藥的 50%，且不超過總資金 1.5%）
    建議單筆上限 = min(建議單日總彈藥 * 0.5, 總資金 * 0.015 * 模式倍率)
    建議單筆上限 = max(建議單筆上限, 單筆下限_twd if 模式倍率 > 0 else 0)

    # 黑天鵝來時可用上限
    黑天鵝單筆上限 = min(黑天鵝預備, 總資金 * 0.05)  # 黑天鵝最多 5%

    return {
        "水位": {
            "持股_pct": round(持股_pct, 2),
            "現金_pct": round(現金_pct, 2),
            "閒錢比_pct": round(閒錢比_pct, 2),
            "目標閒錢比_pct": 目標閒錢比_pct,
            "差距_pct": round(差距, 2),
        },
        "模式": 模式,
        "模式emoji": 模式emoji,
        "模式說明": 說明,
        "模式倍率": 模式倍率,
        "可動用彈藥_twd": round(可動用彈藥, 0),
        "閒錢底線_twd": round(閒錢底線_twd, 0),
        "平時可用_twd": round(平時可用, 0),
        "黑天鵝預備金_twd": round(黑天鵝預備, 0),
        "建議單日總彈藥_twd": round(建議單日總彈藥, 0),
        "建議單筆上限_twd": round(建議單筆上限, 0),
        "黑天鵝單筆上限_twd": round(黑天鵝單筆上限, 0),
        "年化部署計畫": {
            "預期年化次數": 預期年化次數,
            "月度平均_twd": round(平時可用 / 12, 0),
            "季度平均_twd": round(平時可用 / 4, 0),
            "單次平均_twd": round(平時可用 / 預期年化次數, 0),
        },
    }


def 套用彈藥限制(原建議: list, 彈藥資訊: dict,
                  黑天鵝中: bool = False) -> dict:
    """
    把 position_sizer 的建議按「優先順序保留」邏輯處理。
    不均分縮放 — 高倍率訊號優先吃飽，剩下的標 PASS。

    回傳：
      {
        調整後清單: [...],   # 仍含全部標的，但 PASS 的金額 = 0
        通過清單: [...],     # 只有今天該買的
        原始總額_twd, 調整後總額_twd,
        說明,
      }
    """
    if not 原建議:
        return {"調整後清單": [], "通過清單": [],
                "原始總額_twd": 0, "調整後總額_twd": 0,
                "說明": "無建議"}

    上限 = (彈藥資訊["黑天鵝單筆上限_twd"] if 黑天鵝中
            else 彈藥資訊["建議單筆上限_twd"])
    日總上限 = (彈藥資訊["黑天鵝預備金_twd"] if 黑天鵝中
                else 彈藥資訊["建議單日總彈藥_twd"])

    # 1. 排序：倍率高的優先（已在 position_sizer 排好）
    排序 = sorted(原建議,
                   key=lambda r: -(r.get("倍率") or 0))

    # 2. 套用單筆上限 + 日總上限
    調整後 = []
    通過 = []
    用掉_twd = 0

    for r in 排序:
        new_r = dict(r)
        原金 = r.get("建議金額_twd", 0) or r.get("建議金額", 0)
        現價 = r.get("現價") or r.get("close") or 1

        # 套單筆上限
        new_金 = min(原金, int(上限))

        # 看日總彈藥還剩多少
        剩餘 = 日總上限 - 用掉_twd
        if 剩餘 < 單筆下限_twd:
            # 沒彈藥了 → PASS
            new_r["建議金額_twd"] = 0
            new_r["建議金額"] = 0
            new_r["建議股數"] = 0
            new_r["_PASS_原因"] = "日彈藥已用完"
            new_r["_受限"] = True
            調整後.append(new_r)
            continue

        # 還有彈藥，吃這筆（但不超過剩餘）
        final_金 = min(new_金, int(剩餘))
        if final_金 < 單筆下限_twd:
            # 剩太少 → PASS
            new_r["建議金額_twd"] = 0
            new_r["建議金額"] = 0
            new_r["建議股數"] = 0
            new_r["_PASS_原因"] = f"剩 NT${剩餘:,.0f} 不夠（< {單筆下限_twd}）"
            new_r["_受限"] = True
            調整後.append(new_r)
            continue

        new_r["建議金額_twd"] = final_金
        new_r["建議金額"] = final_金
        new_r["建議股數"] = max(1, int(final_金 / 現價)) if 現價 > 0 else 0
        new_r["_受限"] = (final_金 < 原金)
        new_r["_原金_twd"] = 原金
        用掉_twd += final_金
        調整後.append(new_r)
        通過.append(new_r)

    原總額 = sum(r.get("建議金額_twd", 0) or r.get("建議金額", 0)
                  for r in 原建議)
    說明 = (f"原始 {len(原建議)} 檔 NT$ {原總額:,.0f} → "
            f"今日採用 {len(通過)} 檔 NT$ {用掉_twd:,.0f} "
            f"(模式 {彈藥資訊['模式emoji']} {彈藥資訊['模式']})")

    return {
        "調整後清單": 調整後,
        "通過清單": 通過,
        "原始總額_twd": 原總額,
        "調整後總額_twd": 用掉_twd,
        "彈藥模式": 彈藥資訊["模式"],
        "說明": 說明,
    }


