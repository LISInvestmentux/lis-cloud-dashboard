def 推導受惠股(關鍵詞: list[str]) -> dict:
    """
    輸入新聞關鍵詞清單，推導受惠/受害個股。
    """
    受惠 = {}  # {symbol: [(name, 理由)]}
    受害 = {}

    for kw in 關鍵詞:
        for 事件, 對應 in 事件對應表.items():
            if kw in 事件 or 事件 in kw:
                for sym, name, *理由_list in 對應.get("受惠", []):
                    理由 = 理由_list[0] if 理由_list else ""
                    if sym not in 受惠:
                        受惠[sym] = {"name": name, "事件清單": [], "理由清單": []}
                    受惠[sym]["事件清單"].append(事件)
                    if 理由:
                        受惠[sym]["理由清單"].append(理由)
                for sym, name, *理由_list in 對應.get("受害", []):
                    理由 = 理由_list[0] if 理由_list else ""
                    if sym not in 受害:
                        受害[sym] = {"name": name, "事件清單": [], "理由清單": []}
                    受害[sym]["事件清單"].append(事件)
                    if 理由:
                        受害[sym]["理由清單"].append(理由)

    # 按命中事件數排序
    受惠排序 = sorted(受惠.items(), key=lambda x: -len(x[1]["事件清單"]))
    受害排序 = sorted(受害.items(), key=lambda x: -len(x[1]["事件清單"]))

    return {
        "受惠": [
            {"symbol": sym, **info, "命中事件數": len(info["事件清單"])}
            for sym, info in 受惠排序
        ],
        "受害": [
            {"symbol": sym, **info, "命中事件數": len(info["事件清單"])}
            for sym, info in 受害排序
        ],
    }


def 今日事件推導() -> dict:
    """
    從今日新聞情緒抓事件關鍵詞 → 推導受惠股
    """
    news_path = 專案根 / "數據" / "daily" / f"news_{datetime.now():%Y-%m-%d}.json"
    if not news_path.exists():
        return {"error": "今日無新聞快取", "建議": "先跑 news_sentiment.今日新聞情緒()"}

    news = json.loads(news_path.read_text(encoding="utf-8"))

    # 收集新聞標題的關鍵詞
    所有關鍵詞 = set()
    for n in news.get("新聞列表", []):
        for 命中 in n.get("命中", {}).values():
            所有關鍵詞.update(命中)

    if not 所有關鍵詞:
        return {"關鍵詞": [], "受惠": [], "受害": []}

    推導 = 推導受惠股(list(所有關鍵詞))
    return {
        "日期": datetime.now().strftime("%Y-%m-%d"),
        "今日新聞關鍵詞": sorted(所有關鍵詞),
        "受惠": 推導["受惠"][:15],
        "受害": 推導["受害"][:10],
        "邏輯": "億級講師：事件→產業→受惠股",
    }


