"""
把握度折扣（Phase 33.2 — 大俠教學）

「系統算我該買 200 股，但我只有 7 成把握，那實際買 140 股。」

設計理念：
  系統的訊號是「客觀分數」（位階、勝率、大盤）
  把握度是「主觀信念」（user 對標的的個別熟悉度、產業判斷、時機感）

  例：
    - 系統算 NVDA 該買 200 股（強訊號）
    - 但 NVDA 已連 4 天漲，user 7 成把握「會繼續漲」→ 買 140 股
    - 若 user 才 3 成把握「可能回檔」→ 買 60 股（不錯過但也不重押）

  保護機制：
    - 最少還是買 1 股（避免完全錯失）
    - 最多不超過系統建議
    - 折扣後低於最小金額警告

入口：
  套折扣(系統建議股數, 把握度=7)          → 7 成 = ×0.7
  套折扣金額(系統建議_twd, 把握度=5, 現價) → 5 成 + 現價反推股數
"""
from typing import Optional


# 把握度等級對應（user 直覺好抓）
等級表 = {
    10: {"倍率": 1.0,  "標籤": "全押",      "色": "bull",
         "解釋": "完全有把握，照系統算"},
    9:  {"倍率": 0.9,  "標籤": "高把握",    "色": "bull",
         "解釋": "幾乎篤定"},
    8:  {"倍率": 0.8,  "標籤": "強信念",    "色": "bull",
         "解釋": "強信念但留 20% buffer"},
    7:  {"倍率": 0.7,  "標籤": "偏多",      "色": "bull",
         "解釋": "看多但保留空間"},
    6:  {"倍率": 0.6,  "標籤": "略偏多",    "色": "wait",
         "解釋": "看多但不太確定"},
    5:  {"倍率": 0.5,  "標籤": "對半",      "色": "wait",
         "解釋": "對半信心，分批測試"},
    4:  {"倍率": 0.4,  "標籤": "略弱",      "色": "wait",
         "解釋": "信心不足但想參與"},
    3:  {"倍率": 0.3,  "標籤": "試水溫",    "色": "wait",
         "解釋": "只想試一下"},
    2:  {"倍率": 0.2,  "標籤": "微試",      "色": "bear",
         "解釋": "幾乎不買，象徵性"},
    1:  {"倍率": 0.1,  "標籤": "象徵",      "色": "bear",
         "解釋": "只買最小單位"},
    0:  {"倍率": 0,    "標籤": "PASS",      "色": "bear",
         "解釋": "完全沒把握，跳過"},
}


def 套折扣股數(系統建議股數: int, 把握度: int = 10,
                最少股數: int = 1) -> dict:
    """
    依把握度折扣系統建議的股數。

    Args:
        系統建議股數: 系統算出來該買幾股
        把握度: 0-10 整數（10 = 全押 / 5 = 對半 / 0 = pass）
        最少股數: 即使 round down 也至少留 N 股（1 股）

    Returns:
        {
            原建議: int,
            把握度: int,
            等級: str,
            倍率: float,
            建議股數: int,    # 套折扣後實際買多少
            折扣股數: int,    # 少買多少
            解釋: str,
        }
    """
    把握度 = max(0, min(10, int(把握度)))
    等級 = 等級表[把握度]
    倍率 = 等級["倍率"]

    if 倍率 <= 0:
        建議 = 0
    else:
        # round down 但至少 1 股
        建議 = max(最少股數, int(系統建議股數 * 倍率))
        # 不超過系統建議
        建議 = min(建議, 系統建議股數)

    return {
        "原建議": 系統建議股數,
        "把握度": 把握度,
        "等級": 等級["標籤"],
        "色": 等級["色"],
        "倍率": 倍率,
        "建議股數": 建議,
        "折扣股數": 系統建議股數 - 建議,
        "解釋": f"{等級['標籤']} ({把握度}/10) — {等級['解釋']}",
    }


def 套折扣金額(系統建議_twd: float, 把握度: int,
                現價: float, USD_TWD: float = 31.5,
                市場: str = "TW") -> dict:
    """
    依把握度折扣系統建議的金額，反推實際股數。

    Args:
        系統建議_twd: 系統建議投入金額（NT$）
        把握度: 0-10
        現價: 當前股價（市場貨幣 — TW 是 NT$ / US 是 USD）
        USD_TWD: 匯率
        市場: TW / US

    Returns:
        {
            原建議_twd, 套折扣_twd, 把握度,
            建議股數, 實際投入_twd, 解釋
        }
    """
    把握度 = max(0, min(10, int(把握度)))
    等級 = 等級表[把握度]
    倍率 = 等級["倍率"]

    套折扣_twd = 系統建議_twd * 倍率
    每股_twd = 現價 * USD_TWD if 市場 == "US" else 現價

    if 每股_twd <= 0 or 倍率 <= 0:
        建議股數 = 0
    else:
        建議股數 = max(1, int(套折扣_twd // 每股_twd))

    實際_twd = 建議股數 * 每股_twd

    return {
        "原建議_twd": round(系統建議_twd, 0),
        "套折扣_twd": round(套折扣_twd, 0),
        "把握度": 把握度,
        "等級": 等級["標籤"],
        "色": 等級["色"],
        "倍率": 倍率,
        "現價": 現價,
        "每股_twd": round(每股_twd, 2),
        "建議股數": 建議股數,
        "實際投入_twd": round(實際_twd, 0),
        "差異_twd": round(系統建議_twd - 實際_twd, 0),
        "解釋": f"{等級['標籤']} ({把握度}/10) — {等級['解釋']}",
    }


# ─────────────────────────────────────────────
# 整合：把整個訊號清單套折扣
# ─────────────────────────────────────────────
def 批次套折扣(建議清單: list, 把握度對應: dict,
                預設把握度: int = 7) -> list:
    """
    對 position_sizer.批次建議() 的結果套把握度折扣。

    Args:
        建議清單: [{symbol, 建議金額_twd, 現價, ...}, ...]
        把握度對應: {symbol: 0-10} — 沒指定的 symbol 用預設
        預設把握度: 沒指定時的把握度（預設 7 = 偏多）

    Returns:
        建議清單加上「把握度資訊」欄位
    """
    結果 = []
    for r in 建議清單:
        symbol = r.get("symbol")
        把握 = 把握度對應.get(symbol, 預設把握度)
        現價 = r.get("現價") or 1
        市場 = "US" if not (symbol.endswith(".TW") or symbol.endswith(".TWO")) else "TW"
        折扣 = 套折扣金額(
            系統建議_twd=r.get("建議金額_twd", 0),
            把握度=把握,
            現價=現價,
            市場=市場,
        )
        r_new = dict(r)
        r_new["把握度資訊"] = 折扣
        r_new["把握後股數"] = 折扣["建議股數"]
        r_new["把握後金額_twd"] = 折扣["實際投入_twd"]
        結果.append(r_new)
    return 結果


