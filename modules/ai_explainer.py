def _挑選關鍵訊號(全部個股: list[dict],
                   上限: int = 3) -> list[dict]:
    """
    從 watchlist 掃描結果挑出最值得 AI 解釋的 N 個訊號。
    優先順序：
      1. 震盪低點（shakeout_score >= 4）
      2. MACD 金叉 + 突破 MA50（bull_signal）
      3. 錯殺（shit_signal，極端超賣）
    """
    候選 = []
    for r in 全部個股:
        if "error" in r:
            continue
        if r.get("shakeout_low"):
            候選.append({
                "個股": r,
                "類型": "震盪低點",
                "分數": 100 + (r.get("shakeout_score") or 0) * 10,
            })
        elif r.get("bull_signal"):
            候選.append({"個股": r, "類型": "金叉突破", "分數": 90})
        elif r.get("shit_signal"):
            候選.append({"個股": r, "類型": "錯殺超賣", "分數": 80})

    候選.sort(key=lambda x: x["分數"], reverse=True)
    return 候選[:上限]


def _建構提示詞(候選: list[dict]) -> str:
    """組 Gemini 提示詞。"""
    訊號段 = []
    for c in 候選:
        r = c["個股"]
        rsi = r.get("rsi14")
        ma50 = r.get("ma50")
        ma200 = r.get("ma200")
        漲幅20日 = r.get("漲幅_20日_pct")
        量爆 = r.get("量爆倍數")
        訊號段.append(
            f"【{r['symbol']} {r.get('name','')}】類型：{c['類型']}\n"
            f"  收盤 {r.get('close')}、RSI {rsi}、"
            f"近 20 日漲幅 {漲幅20日}%、量能倍數 {量爆}\n"
            f"  MA50={ma50}、MA200={ma200}\n"
            f"  觸發原因：{'、'.join(r.get('shakeout_reasons', []) or [c['類型']])}"
        )
    所有訊號 = "\n\n".join(訊號段)

