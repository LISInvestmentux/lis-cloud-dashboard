def 解釋關鍵訊號(全部個股: list[dict], 上限: int = 3,
                  retries: int = 2) -> list[dict]:
    """
    回傳 [{symbol, name, 類型, 解釋}, ...]
    若 Gemini 呼叫失敗，回傳空 list（不影響主流程）。
    """
    if not GEMINI_API_KEY:
        return []

    候選 = _挑選關鍵訊號(全部個股, 上限)
    if not 候選:
        return []

    提示詞 = _建構提示詞(候選)

    client = genai.Client(api_key=GEMINI_API_KEY)

    回應文字 = None
    for i in range(retries):
        try:
            resp = client.models.generate_content(
                model=MODEL,
                contents=提示詞,
                config=types.GenerateContentConfig(
                    temperature=0.4,
                    max_output_tokens=600,
                ),
            )
            回應文字 = (resp.text or "").strip()
            break
        except Exception as e:
            if i == retries - 1:
                print(f"  ⚠️ Gemini 呼叫失敗：{e}")
                return []
            time.sleep(1)

    # 解析「[代號] | 解釋」格式
    結果 = []
    for c in 候選:
        sym = c["個股"]["symbol"]
        # 找對應行
        解釋 = ""
        for line in (回應文字 or "").splitlines():
            line = line.strip()
            if not line:
                continue
            # 容錯多種格式：[2330.TW] | xxx / 2330.TW | xxx / **2330.TW**: xxx
            if sym in line or sym.replace(".TW", "") in line:
                # 拿 | 後面的，沒有就拿 : 後面的
                if "|" in line:
                    解釋 = line.split("|", 1)[1].strip()
                elif ":" in line:
                    解釋 = line.split(":", 1)[1].strip()
                elif "：" in line:
                    解釋 = line.split("：", 1)[1].strip()
                else:
                    解釋 = line
                # 移除 markdown
                解釋 = 解釋.replace("**", "").replace("```", "").strip()
                break

        if 解釋:
            結果.append({
                "symbol": sym,
                "name": c["個股"].get("name", ""),
                "類型": c["類型"],
                "解釋": 解釋,
            })

    return 結果
