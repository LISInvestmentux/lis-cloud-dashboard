def 分析文字(內容: str, 來源: str = "") -> dict:
    """
    對一段文字做 7 紅旗判定。

    Args:
        內容: 要分析的文字（PDF / LINE 訊息 / 文章等）
        來源: 來源說明（給 user 看的）

    Returns:
        {
            紅旗命中: [{類型, 範例, 命中關鍵詞, 命中次數}, ...],
            紅旗數: int,
            總權重: int,
            風險等級: "低 / 中 / 高 / 極高",
            色: "bull / wait / bear / 必做",
            建議: str,
        }
    """
    if not 內容:
        return {"紅旗命中": [], "紅旗數": 0, "總權重": 0,
                "風險等級": "低", "色": "bull",
                "建議": "（空內容）"}

    命中清單 = []
    總權重 = 0
    for 類型, 規則 in 紅旗規則.items():
        命中關鍵詞 = []
        命中次數 = 0
        for pat in 規則["關鍵詞"]:
            matches = re.findall(pat, 內容, re.IGNORECASE)
            if matches:
                命中關鍵詞.append({
                    "pattern": pat,
                    "matches": matches[:5],
                })
                命中次數 += len(matches)
        if 命中關鍵詞:
            命中清單.append({
                "類型": 類型,
                "範例": 規則["範例"],
                "權重": 規則["權重"],
                "命中關鍵詞": 命中關鍵詞,
                "命中次數": 命中次數,
            })
            總權重 += 規則["權重"]

    # 風險判定
    紅旗數 = len(命中清單)
    if 紅旗數 == 0:
        等級 = "低"; 色 = "bull"
        建議 = "未發現典型詐騙模式（但仍要保持警覺）"
    elif 紅旗數 <= 2 and 總權重 <= 4:
        等級 = "中"; 色 = "wait"
        建議 = "有部分行銷話術，可保留懷疑態度評估"
    elif 紅旗數 <= 4 and 總權重 <= 8:
        等級 = "高"; 色 = "bear"
        建議 = "詐騙特徵明顯，強烈建議不要付費 / 不要加群 / 不要跟單"
    else:
        等級 = "極高"; 色 = "必做"
        建議 = "🚨 典型詐騙！立刻封鎖 / 檢舉 / 提醒家人朋友"

    return {
        "來源": 來源,
        "紅旗命中": 命中清單,
        "紅旗數": 紅旗數,
        "總權重": 總權重,
        "風險等級": 等級,
        "色": 色,
        "建議": 建議,
    }


def 印報告(結果: dict) -> str:
    """把分析結果格式化成可讀的報告（給 LINE / CLI 用）"""
    片段 = []
    片段.append(f"🛡️ 反詐騙分析 — {結果.get('來源', '')}")
    片段.append(f"━━━━━━━━━━━━━━━━")
    片段.append(f"風險等級：{結果['風險等級']}")
    片段.append(f"紅旗數：{結果['紅旗數']} 個 / 權重 {結果['總權重']}")
    片段.append(f"")

    if 結果["紅旗命中"]:
        片段.append("🚩 命中紅旗：")
        for r in 結果["紅旗命中"]:
            片段.append(f"  • {r['類型']} (權重 {r['權重']}) — {r['範例']}")
            for k in r["命中關鍵詞"][:3]:
                ex = k["matches"][:2]
                片段.append(f"      命中：{ex}")

    片段.append("")
    片段.append(f"💡 {結果['建議']}")
    return "\n".join(片段)


